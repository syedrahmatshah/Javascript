// 10.4 Math.round()
// The Math.round() static method returns the value of a number rounded to the nearest integer.

// It give the upper value when the number is greater than or equal to 0.5
let a = Math.round(3.56);
console.log(a);

// It give the lower value when the number is less than to 0.5
let b = Math.round(3.26);
console.log(b);

let c = Math.round(-21.5);
console.log(c);
