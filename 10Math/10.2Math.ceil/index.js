// 10.2 Math.ceil
// The Math.ceil() static method always rounds up and returns
// the smallest integer greater than or equal to a given number.

// It give the upper nearist value
// It work when the number is in decimal form

let a = Math.ceil(5.3);
console.log(a);

// What happen when we give it value in negative
let b = Math.ceil(-8.56);
console.log(b);

let c = Math.ceil(4);
console.log(c);

let d = Math.ceil("5");
console.log(d);

let e = Math.ceil();
console.log(e);

let f = Math.ceil(true);
console.log(f);
