// 10.7 Math.min()
// The Math.min() static method returns the smallest of the
// numbers given as input parameters, or Infinity if there are no parameters.

let a = Math.min(10, 20);
console.log(a);

let b = Math.min(-10, -20);
console.log(b);

let c = Math.min(-10, 20);
console.log(c);

let d = Math.min();
console.log(d);
