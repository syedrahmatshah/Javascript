// 10.3 Math.floor()
// The Math.floor() static method always rounds down and
// returns the largest integer less than or equal to a given number.

//It give the downward value of the given number
let a = Math.floor(7.44);
console.log(a);

let b = Math.floor(-45.49);
console.log(b);

let c = Math.floor(-0);
console.log(c);

let d = Math.floor(0.89);
console.log(d);

let e = Math.floor(true);
console.log(e);
