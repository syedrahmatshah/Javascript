// 10.1 Math.abs()
// The Math.abs() static method returns the absolute value of a number.

const a = Math.abs(-23.34);
console.log(a);

const b = Math.abs("2");
console.log(b);

const c = Math.abs("-33");
console.log(c);

const d = Math.abs("string");
console.log(d);

const e = Math.abs(null);
console.log(e);

const f = Math.abs(true);
console.log(f);

const g = Math.abs(false);
console.log(g);

const h = Math.abs("");
console.log(h);

const i = Math.abs();
console.log(i);
