// 10.6 Math.max()
// The Math.max() static method returns the largest of the numbers
// given as input parameters, or -Infinity if there are no parameters.

let a = Math.max(10, 20);
console.log(a);

let b = Math.max(-10, -20);
console.log(b);

let c = Math.max(-10, 20);
console.log(c);

let d = Math.max();
console.log(d);
