// 10.5 Math.trunc()
// The Math.trunc() static method returns the integer part of a number by removing any fractional digits.

let a = Math.trunc("-1.123");
console.log(a);

let b = Math.trunc(-0.123);
console.log(b);

let c = Math.trunc(0.123);
console.log(c);

let d = Math.trunc(13.37);
console.log(d);

let e = Math.trunc(42.84);
console.log(e);
