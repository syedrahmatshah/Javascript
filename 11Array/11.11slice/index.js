// 11.11 slice()
/*
1. The JavaScript array slice() method extracts the part of the given array and returns it. 
2. This method doesn't change the original array.
*/
let arr = ["AngularJS", "Node.js", "JQuery", "Bootstrap"];
let result = arr.slice(1, 2);
console.log(result);

var arr1 = ["AngularJS", "Node.js", "JQuery", "Bootstrap"];
var result1 = arr.slice(0, 3);
console.log(result1);
