// 11.16  toString()
/*
1. The toString() method is used for converting and representing an array into string form. 
2. It returns the string containing the specified array elements.
3. Commas separate these elements, and the string does not affect the original array.
*/

let arr = ["j", "a", "v", "a", "T", "p", "o", "i", "n", "t"]; //array elements
let str = arr.toString(); //toString() method implementation
console.log(str);

let season = ["Spring", "Autumn", "Summer", "Winter"];
let str2 = season.toString(); //toString() method implementation
console.log(str2);
